#04 Data Type


#set x as int
x = 123
print(type(x))

#set x as str
x = '123'
print(type(x))

#set x as float
x = 123.12
print(type(x))

#set x as complex
x = 123+2j
print(type(x))

#set x as bool
x = True
print(type(x))

#set x as tuple
x = (123,456)
print(type(x))

#set x as list
x = [123]
print(type(x))

#set x as set
x = {123}
print(type(x))

#set x as dictv
x = {1:123}
print(type(x))
